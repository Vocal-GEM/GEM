/**
 * Get a random sentence from a collection
 */
export const getRandomSentence = (collection) => {
    return collection[Math.floor(Math.random() * collection.length)];
};

/**
 * Get sentences by category
 */
export const getSentencesByCategory = (category) => {
    switch (category) {
        case 'harvard':
            return HARVARD_SENTENCES;
        case 'poetry':
            return POETRY_LINES.map(p => p.text);
        case 'rainbow':
            return RAINBOW_PASSAGE;
        case 'front-vowel':
            return FRONT_VOWEL_SENTENCES;
        case 'intonation':
            return INTONATION_SENTENCES.map(s => s.text);
        case 'articulation':
            return ARTICULATION_SENTENCES;
        case 'conversational':
            return CONVERSATIONAL_PHRASES;
        default:
            return HARVARD_SENTENCES;
    }
};

/**
 * Get all sentences combined
 */
export const getAllSentences = () => [
    ...HARVARD_SENTENCES,
    ...POETRY_LINES.map(p => p.text),
    ...RAINBOW_PASSAGE,
    ...FRONT_VOWEL_SENTENCES,
    ...INTONATION_SENTENCES.map(s => s.text),
    ...ARTICULATION_SENTENCES,
    ...CONVERSATIONAL_PHRASES
];

